# Operadores Relacionais
# Neste programa mostro os principais 
# operadores relacionais e suas comparações 
# utilizando as variáveis X e Y

x = 2
y = 3

print(x == y) # igual
print(x < y)  # menor
print(x > y)  # maior
print(x >= y) # maior ou igual
print(x <= y) # menor ou igual
print(x != y) # diferente